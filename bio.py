# ---------------------- 2. 最长匹配标注函数
def label_sentence(sentence):
    chars = list(sentence)
    labels = ["O"] * len(chars)
    # 收集所有关键词，按长度降序（最长匹配优先）
    all_keywords = []
    for category, words in entity_keywords.items():
        for word in words:
            all_keywords.append((word, category))
    # 按关键词长度降序，避免短词截断长词
    all_keywords.sort(key=lambda x: len(x[0]), reverse=True)

    for word, category in all_keywords:
        word_len = len(word)
        i = 0
        while i <= len(chars) - word_len:
            if "".join(chars[i:i + word_len]) == word:
                labels[i] = f"B-{category}"
                for j in range(i + 1, i + word_len):
                    labels[j] = f"I-{category}"
                i += word_len  # 跳过已匹配部分
            else:
                i += 1
    return chars, labels


# ---------------------- 3. 读取新文本，生成标注
def generate_bio_data(input_txt_path, train_output_path, test_output_path, test_ratio=0.1):
    # 读取新文本
    with open(input_txt_path, "r", encoding="utf-8") as f:
        sentences = [line.strip() for line in f if line.strip()]

    # 标注所有句子
    bio_data = []
    for sent in sentences:
        chars, labels = label_sentence(sentence=sent)
        bio_data.append(("".join(chars), " ".join(labels)))

    # 划分训练集/测试集（按比例）
    import random
    random.shuffle(bio_data)
    split_idx = int(len(bio_data) * (1 - test_ratio))
    train_data = bio_data[:split_idx]
    test_data = bio_data[split_idx:]

    # 写入训练集
    with open(train_output_path, "w", encoding="utf-8") as f:
        for sent, label in train_data:
            f.write(f"{sent}\n{label}\n\n")
    # 写入测试集
    with open(test_output_path, "w", encoding="utf-8") as f:
        for sent, label in test_data:
            f.write(f"{sent}\n{label}\n\n")
    print(f"✅ 生成完成！")
    print(f"📊 总数据：{len(bio_data)} 条")
    print(f"📈 训练集：{len(train_data)} 条")
    print(f"📉 测试集：{len(test_data)} 条")


